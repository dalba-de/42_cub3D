#include "../includes/cub3d.h"

void	ft_bzero(void *str, size_t n)
{
	char	*c;
	size_t	i;

	c = (char*)str;
	if (n != 0)
	{
		i = 0;
		while (i < n)
		{
			c[i] = '\0';
			i++;
		}
	}
}
